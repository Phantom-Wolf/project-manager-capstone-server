CREATE TABLE users (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    username VARCHAR (255) UNIQUE NOT NULL,
    user_email VARCHAR (255) UNIQUE NOT NULL,
    user_password VARCHAR (225) NOT NULL
);

CREATE TABLE projects (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    project_name VARCHAR (255) NOT NULL,
    user_id INTEGER REFERENCES users(id) ON DELETE CASCADE NOT NULL
);

CREATE TABLE parenttask (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    project_id INTEGER REFERENCES projects(id) ON DELETE CASCADE NOT NULL,
    title VARCHAR (255) NOT NULL,
    task_level INTEGER NOT NULL,
    completion_status BOOLEAN DEFAULT FALSE NOT NULL
);

CREATE TABLE parentnote (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    parent_id INTEGER REFERENCES parenttask(id) ON DELETE CASCADE NOT NULL,
    user_id INTEGER REFERENCES users(id) ON DELETE CASCADE NOT NULL,
    note TEXT NOT NULL,
    date_created TIMESTAMP DEFAULT now() NOT NULL
);

CREATE TABLE taskone (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    project_id INTEGER REFERENCES projects(id) ON DELETE CASCADE NOT NULL,
    parent_id INTEGER REFERENCES parenttask(id) ON DELETE CASCADE NOT NULL,
    title VARCHAR (255) NOT NULL,
    task_level INTEGER NOT NULL,
    completion_status BOOLEAN DEFAULT FALSE NOT NULL
);

CREATE TABLE noteone (
